    private void btnXuatSQLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatSQLActionPerformed
        // TODO add your handling code here:
        int result = JOptionPane.showConfirmDialog(this, "Nếu trong thư mục " +jLabel2.getText()+" đã có sẵn dữ liệu sao lưu thì nó sẽ bị ghi đè\nHành động này không thể hoàn tác!", "Cảnh báo", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (result == JOptionPane.YES_OPTION)
        {
            String path = jLabel2.getText();
            File file = new File(path + "\\admin.dmp");
                if (file.exists()) {
                    file.delete();
                }
                file = new File(path + "\\expdpadmin.log");
                if (file.exists()) {
                    file.delete();
                }
             try {
                
                oracle.openConnection();
                String sql = "CREATE OR REPLACE DIRECTORY dmpdir AS '" + path + "'";
                Statement st = oracle.conn.createStatement();
                st.executeUpdate(sql);
                oracle.closeConnection();
                
    
             } catch (Exception ex) {
                  JOptionPane.showMessageDialog(null, "Lỗi truy vấn: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
                // TODO: handle exception
             }
             
            try {
                
                //String cmd = "expdp '/ as sysdba' directory=dmpdir dumpfile=export_schema.dmp logfile=export_schema.log schemas=admin";
                String cmd="expdp \"'/ as sysdba'\" schemas=admin directory=DMPDIR dumpfile=admin.dmp logfile=expdpadmin.log";
                ProcessBuilder pb = new ProcessBuilder("cmd.exe", "/c", cmd);
                pb.redirectErrorStream(true);
                Process p = pb.start();
                BufferedReader in = new BufferedReader(new InputStreamReader(p.getInputStream()));
                String line = null;
                while ((line = in.readLine()) != null) {
                    jTextArea1.append(line+"\n");
                    System.out.println(line);
                    
                }
                in.close();
                p.waitFor();
                JOptionPane.showMessageDialog(null, "Xong!");
            } catch (IOException e) {
                    
            } catch (InterruptedException e) {
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Xuất thất bại");
        }

        
        

        

    }//GEN-LAST:event_btnXuatSQLActionPerformed

